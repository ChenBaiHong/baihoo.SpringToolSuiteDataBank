drop table user if exists;
create table user (
	objectid bigint generated by default as identity comment '唯一识别ID，并自动增长',
	username varchar(40) comment '用户名称',
	password varchar (40) comment '密码',
	name varchar(20) comment '真实名称',
	age int(3) comment '年龄',
	balance decimal (10, 2) comment '账目清单',
	primary key(objectid)
);
